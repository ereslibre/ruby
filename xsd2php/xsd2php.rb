$: << File.expand_path(File.dirname(__FILE__))

require "find"
require "colorize"
require "xmlsimple"
require "fileutils"
require "progressbar"

require "phpclass"

if ARGV.size != 2
  puts("usage: #$0 rootXSDdirectory destinationDirectory")
  puts
  puts("\trootXSDdirectory\tThe root directory that contains all XSD files")
  puts("\tdestinationDirectory\tThe destination directory where autogenerated files will be created")
  Process.exit
end

root = ARGV[0]
destination = ARGV[1]

rootDir = File.open(root, "r")
fileList = Array.new
Find.find(rootDir) do |path|
  fileList << path if path =~ /(.+).xml$/
end
rootDir.close

FileUtils.mkdir_p(destination)

progress = ProgressBar.new("Generating", fileList.count * 3)

fileContents = Array.new
for file in fileList
  fileContents << XmlSimple.xml_in(file, { "KeyAttr" => "name" })
  progress.inc
end

phpClasses = Array.new
for contents in fileContents
  phpClasses << PHPClass.new(destination, contents)
  progress.inc
end

for phpClass in phpClasses
  phpClass.writeClass(phpClasses)
  progress.inc
end

puts
puts "***".light_green
puts "*** Successful generation".light_green
puts "***".light_green
puts "*** Destination: #{File.expand_path(destination)}".light_green
puts "***".light_green
